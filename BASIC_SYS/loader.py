import os
from json import load

### ==========================================================================.

### ==========================================================================.
# loader.

def LOADER(DIR, NAME):
    try:
        with open(os.path.join(DIR, NAME), mode="r") as f:
            DATA = load(f)
        return True, DATA
    except:
        return False, None